function toPascalCase(snake) {
  return snake.split('_').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join('');
}

function getAllColumns(table) {
  const cols = [`${table.name}_id`, ...table.columns.map(c => c.name)];
  if (!table.skipModified) cols.push('modified');
  cols.push('created');
  return cols;
}

function pkParam(table) {
  return `@p_${toPascalCase(table.name)}RecordId`;
}

function colParam(col) {
  return `@p_${toPascalCase(col.name)}`;
}

// ============================================================
// View helpers
// ============================================================

function getViewColumns(view) {
  const cols = [`${view.baseTable}_id`];
  for (const c of view.baseCols) cols.push(c);
  for (const join of view.joins) {
    for (const c of join.cols) {
      cols.push(typeof c === 'string' ? c : c.as);
    }
  }
  const audit = view.auditCols || ['modified', 'created'];
  for (const c of audit) cols.push(c);
  return cols;
}

