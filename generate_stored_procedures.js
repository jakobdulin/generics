if (require.main === module) {
  const outputBase = process.argv[3] ? path.resolve(process.argv[3]) : __dirname;
  const viewsJsonPath = process.argv[4] ? path.resolve(process.argv[4]) : null;
  const views = viewsJsonPath ? require(viewsJsonPath) : [];

  const SP_DIR = path.join(outputBase, 'STORED_PROCEDURES');

  if (!fs.existsSync(SP_DIR)) {
    fs.mkdirSync(SP_DIR, { recursive: true });
  }

  let count = 0;

  // Table SPs
  for (const table of tables) {
    const sps = [
      [`sx_get_${table.name}`, generateGet(table)],
      [`sx_list_${table.name}`, generateList(table)],
      [`sx_upsert_${table.name}`, generateUpsert(table)],
    ];

    const hasIsActive = table.columns.some(c => c.name === 'is_active');
    if (hasIsActive) {
      sps.push([`sx_delete_${table.name}`, generateDelete(table, tables)]);
    }

    if (table.hasHistory) {
      sps.push([`sx_list_${table.name}_history`, generateListHistory(table)]);
    }

    for (const [spName, sql] of sps) {
      fs.writeFileSync(path.join(SP_DIR, `${spName}.sql`), sql, 'utf8');
      count++;
    }
  }

  // View SPs
  for (const view of views) {
    const sps = [
      [`sx_get_${view.name}`, generateViewGet(view)],
      [`sx_list_${view.name}`, generateViewList(view)],
    ];

    for (const [spName, sql] of sps) {
      fs.writeFileSync(path.join(SP_DIR, `${spName}.sql`), sql, 'utf8');
      count++;
    }
  }

  // sx_test — connection diagnostic procedure
  const testSql = `-- ABSOLUTELY DO NOT EDIT THIS FILE. IT WAS GENERATED BY generate_stored_procedures.js. DO NOT MODIFY OR REMOVE THIS COMMENT UNDER PENALTY OF CERTAIN DEATH.
SET QUOTED_IDENTIFIER ON;
GO
IF EXISTS (SELECT * FROM sys.procedures WHERE name = 'sx_test')
    DROP PROCEDURE sx_test;
GO

CREATE PROCEDURE sx_test
AS
BEGIN
    SET NOCOUNT ON;
    SELECT
        'pass' [test],
        encrypt_option [encryption],
        auth_scheme [auth_scheme]
    FROM sys.dm_exec_connections
    WHERE session_id = @@SPID;
END;
GO
`;
  fs.writeFileSync(path.join(SP_DIR, 'sx_test.sql'), testSql, 'utf8');
  count++;

  console.log(`Generated ${count} stored procedure files in ${SP_DIR}`);
}

